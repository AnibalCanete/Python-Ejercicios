
# Caso 1 - Bucle Finito
# Creamos la variable (iterador), con valor cero
iterador = 0

# Con el (while), establecemos la condición que debe cumplirse
while iterador < 10:
    print(iterador) # Imprimimos la Variable
    iterador = iterador + 1 # Incrementamos la variable, cada vez que se cumpla la condición
