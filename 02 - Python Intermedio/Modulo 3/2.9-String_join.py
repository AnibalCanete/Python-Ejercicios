
# La función (join) nos permite separar caracter por caracter, con la opción que coloquemos antes
palabra = "Hola" # Creamos la variable (palabra), con su respectivo valor
print(palabra) # Imprimimos la variable (palabra) para visualizar los cambios posteriores
print("-".join(palabra)) # Imprimimos la variable (palabra) ya con el efecto de la función (join)
