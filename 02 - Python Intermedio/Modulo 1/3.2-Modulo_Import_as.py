
# Podemos utilizar el (import) y traer el modulo (math) y con el (as) le damos un alias al modulo, en este caso (mt)
import math as mt

print(mt.pow(10, 2)) # (pow) quiere indicar una operación potencia
print(mt.sqrt(25)) # (sqrt) quier indicar una operación de raíz cuadrada
print(mt.factorial(10)) # (factorial) quiere indicar una operación factorial
print(mt.cos(45)) # (cos) quiere indicar una operación con coseno
