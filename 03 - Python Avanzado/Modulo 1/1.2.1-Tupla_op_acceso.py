
# Dentro de una Tupla tambien puede ir otra Tupla
tupla = 1, 2, ("a", "b"), 3
print(tupla) # Imprimimos la Tupla
print(tupla[2][0]) # Imprimimos el tercer elemento de la tupla, pero como dicho elemento es una tupla podemos agregar un indice mas para llamar a un elemento de esa tupla
print(tupla[0]) # Imprimimos el primer elemento de la tupla
print(tupla[1]) # Imprimimos el segundo elemento de la tupla
print(tupla[3]) # Imprimimos el cuarto elemento de la tupla
