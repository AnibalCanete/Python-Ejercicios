
# La función (replace) nos permite reemplazar una palabra que se encuentra en la variable (palabras) por otra palabra
palabras = "Variables Globales y Variables Locales" # Creamos la variable (palabras) 
palabras = palabras.replace("Locales", "Local") # El primer parametro de la función (replace), nos indica la palabra que vamos a reemplazar y el segundo es la nueva palabra que pondremos
print(palabras) # Imprimimos la variable (palabras)
