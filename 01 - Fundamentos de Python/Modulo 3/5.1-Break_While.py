
# Declaramos la variable (i) con el valor (1)
i = 1

# Con el (while), recorremos la variable (i) mientras que el valor de (i) sea menor que 100
while i < 100:
    print(i) # Imprimimos la variable

    if i == 5:
        # Con el (break) rompemos el bucle
        break

    i += 1 # Incrementamos la variable (i)

