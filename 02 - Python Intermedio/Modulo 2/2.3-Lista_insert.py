
# Creamos la variable (lista_letras) con sus respectivos valores
lista_letras = ["a", "b", "c", "d", "e"]
print(lista_letras) # Imprimimos la lista sin ninguna inserción

# El (insert()) requiere dos argumentos, el primero seria la posición y el segundo, el valor
lista_letras.insert(0, "A") # En este caso, insertamos en la posición (0), el valor (A)
lista_letras.insert(2, "B") # En este caso, insertamos en la posición (2), el valor (B)
lista_letras.insert(4, "C") # En este caso, insertamos en la posición (4), el valor (C)
print(lista_letras) # Imprimimos la lista ya con inserciones
