
# Creamos el diccionario (d), con sus respectivos valores
d = {
    "A": 1,
    "B": 2
}

k = d.keys() # Con la función (keys) obtenemos un listado de todas las llaves del diccionario
print(k) # Imprimimos los valores de la variable (k)
print(list(k)) # Convertimos los elementos de la variable (k) en una lista con la función (list) y los imprimimos

