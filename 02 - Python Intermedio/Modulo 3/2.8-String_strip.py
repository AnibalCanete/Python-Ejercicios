
# La función (strip) elimina los espacios iniciales y los espacios finales
texto = " Hoy se Estrena la Pelicula de Dr. Strange " # Creamos la variable (texto), con su respectivo valor
print(texto) # Imprimos la variable (texto) sin modificaciones para poder visualizar los cambios posteriores
print(texto.strip()) # Imprimimos la variable (texto) ya con el efecto de la función (strip)
