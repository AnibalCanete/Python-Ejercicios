
# Operadores Aritmeticos
# Creación de Variables
x = 100
y = 10

# Imprimimos las Variables
print("Valor de x = ", x)
print("Valor de y = ", y)
print("x + y = ", x + y)
print("x - y = ", x - y)
print("x * y = ", x *y)

# División Flotante
x, y = 13, 2
print("x / y = ", x / y) # División con Decimales

# Modulo (Nos Muestra El Residuo de una División)
x, y = 13, 2
print("x % y = ", x % y)

# Exponenciales/Potencia
x, y = 2, 5
print("x ** y = ", x ** y)

# División de Piso
x, y = 13 , 2
print("x // y = ", x // y)
