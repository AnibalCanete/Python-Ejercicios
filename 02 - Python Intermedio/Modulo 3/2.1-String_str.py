
# La función (str), nos permite convertir el valor que tengamos en un string para poder concatenarlo
pi = 3.14 # Declaramos la variable (pi), con su valor (3.14)
texto = "El valor de pi es: " + str(pi) # Creamos la variable (texto) y concatenamos su valor, con la función (str) convertimos en string el valor de la variable (pi)
print(texto) # Imprimimos la variable (texto)

