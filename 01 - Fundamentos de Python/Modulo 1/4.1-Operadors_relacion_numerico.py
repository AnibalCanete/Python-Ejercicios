
# Operadores Relacionales (Nos Dara Resultado con True/Verdadero o False/Falso)
# Enteros y Flotantes
x = 35
y = 30

# Imprimimos las Variables
print("x == y :", x == y) # Nos indican si los valores son iguales
print("x != y :", x != y) # Nos indican si los valores son diferentes
print("x < y :", x < y) # Nos indica si el valor de (x) es menor al valor de (y)
print("x > y :", x > y) # Nos indica si el valor de (x) es mayor que el valor de (y)
print("x <= y :", x <= y) # Nos indica si el valor de (x) es menor o igual que el valor de (y)
print("x >= y :", x >= y) # Nos indica si el valor de (x) es mayor o igual que el valor de (y) 
