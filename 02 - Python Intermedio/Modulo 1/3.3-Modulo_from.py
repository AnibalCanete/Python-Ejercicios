
# Con el (from) indicamos el modulo que utilizaremos, y con el (import) importamos las funciones que utilizaremos
from math import pow, sqrt, factorial, cos

print(pow(10, 2)) # (pow) quiere indicar una operación potencia
print(sqrt(25)) # (sqrt) quiere indicar una operación de raíz cuadrada
print(factorial(10)) # (factorial) quier indicar una operación factorial
print(cos(45)) # (cos) quiere indicar una operación con coseno
