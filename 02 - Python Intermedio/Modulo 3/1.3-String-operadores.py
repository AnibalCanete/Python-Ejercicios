
# Concatenar +
nombre = "Lucas" # Creamos la variable (nombre), con su respectivo valor
apellido = "Villalba" # Creamos la variable (apellido), con su respectivo valor 
# Creamos la variable (resultado), y concatenar la variable (nombre), con una variable mas agregamos el espacio (" "), y por ultimo concatenamos con la variable (apellido)
resultado = nombre + " " + apellido 
print(resultado) # Imprimimos la variable (resultado)

# Multiplicación *
palabra = "Hola" # Creamos la variable (palabra) con su respectivo valor
print(palabra*20) # Con la multiplicación, lo que hace es repetir el valor de la variable, es decir, va a repetir el valor de la variable por la cantidad que se multiplica
