
# Función Suma
def Suma(a, b):
    resultado = a + b
    return resultado

# Función Resta
def Resta(a, b):
    resultado = a - b
    return resultado

# Función Multiplicación
def Multiplicacion(a, b):
    resultado = a * b
    return resultado

# Función División
def Division(a, b):
    resultado = a / b
    return resultado

# Función con un parametro 
def funcion_parametros(a):
    print(a)

# Llamamos a la función (funcion_parametros)
funcion_parametros("Soy una Funciín con 1 Parametro")
