
# For con un lista 
# Declaramos la variable (elementList), escribiendo entre los corchetes creamos una lista
elementList = [1, 2, 3, 4, 5]

# Con el (for) recogemos los valores de la lista (item), con el (in) seguido de la variable, indicamos que variable debe recorrer
for item in elementList:
    print("Este es un Elemento de la Lista", item) # Imprimimos los elementos de la lista
    